    public void readJTable() {
        DefaultTableModel dtmEstados = (DefaultTableModel) jTableClientes.getModel();
        dtmEstados.setNumRows(0);
        ClienteDAO cdao = new ClienteDAO();

        for (Cliente c : cdao.read()) {

            dtmEstados.addRow(new Object[]{
                c.getCod(),
                c.getNome()
            });

        }
    }

