    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Excluir cliente existente

        if (jTableClientes.getSelectedRow() != -1) {

            cliente.setCod((int) jTableClientes.getValueAt(jTableClientes.getSelectedRow(), 0));

            clienteDao.delete(cliente);

            txtNome.setText("");
            txtTipoPessoa.setText("");
            txtCpfCnpj.setText("");
            txtCEP.setText("");
            txtEndereco.setText("");
            txtNumero.setText("");
            txtComplemento.setText("");
            txtBairro.setText("");
            txtEstado.setText("");
            txtCidade.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
            txtSituacao.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para excluir");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Incluir novo Cliente

        cliente.setNome(txtNome.getText());
        cliente.setTipopessoa(txtTipoPessoa.getText());
        cliente.setCep(Integer.parseInt(txtCEP.getText()));
        cliente.setEndereco(txtEndereco.getText());
        cliente.setNumero(Integer.parseInt(txtNumero.getText()));
        cliente.setComplemento(txtComplemento.getText());
        cliente.setBairro(txtBairro.getText());
        cliente.setEstado(txtEstado.getText());
        cliente.setCidade(txtCidade.getText());
        cliente.setEmail(txtEmail.getText());
        cliente.setTelefone(Integer.parseInt(txtTelefone.getText()));
        cliente.setSituacao(txtSituacao.getText());

        clienteDao.save(cliente);

        txtNome.setText("");
        txtTipoPessoa.setText("");
        txtCpfCnpj.setText("");
        txtCEP.setText("");
        txtEndereco.setText("");
        txtNumero.setText("");
        txtComplemento.setText("");
        txtBairro.setText("");
        txtEstado.setText("");
        txtCidade.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtSituacao.setText("");

        readJTable();

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Alterar Cadastro de Cliente

        if (jTableClientes.getSelectedRow() != -1) {

            cliente.setNome(txtNome.getText());
            cliente.setTipopessoa(txtTipoPessoa.getText());
            cliente.setCep(Integer.parseInt(txtCEP.getText()));
            cliente.setEndereco(txtEndereco.getText());
            cliente.setNumero(Integer.parseInt(txtNumero.getText()));
            cliente.setComplemento(txtComplemento.getText());
            cliente.setBairro(txtBairro.getText());
            cliente.setEstado(txtEstado.getText());
            cliente.setCidade(txtCidade.getText());
            cliente.setEmail(txtEmail.getText());
            cliente.setTelefone(Integer.parseInt(txtTelefone.getText()));
            cliente.setSituacao(txtSituacao.getText());

            cliente.setCod((int) jTableClientes.getValueAt(jTableClientes.getSelectedRow(), 0));

            clienteDao.update(cliente);

            txtNome.setText("");
            txtTipoPessoa.setText("");
            txtCpfCnpj.setText("");
            txtCEP.setText("");
            txtEndereco.setText("");
            txtNumero.setText("");
            txtComplemento.setText("");
            txtBairro.setText("");
            txtEstado.setText("");
            txtCidade.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
            txtSituacao.setText("");

            readJTable();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

